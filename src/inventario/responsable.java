    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
                conexion cnx = new conexion();
        Connection registro = cnx.conexion();
        String sql = "INSERT INTO responsable(id_responsable,nombre,apellido,telefono,direccion) VALUES (?,?,?,?,?)";
        try {
            PreparedStatement pst = (PreparedStatement) registro.prepareStatement(sql);
            pst.setString(1, this.id_responsable.getText());
            pst.setString(2, this.nombre.getText());
            pst.setString(3, this.apellido.getText());
            pst.setString(4, this.telefono.getText());
            pst.setString(5, this.direccion.getText());
            int numero_datos = pst.executeUpdate();
            if (numero_datos > 0) {
                JOptionPane.showMessageDialog(null, "Se ingresaron  Correctamente los datos", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
         
            } else {
                JOptionPane.showMessageDialog(null, "Campos vacios\n Ingrese sus datos", "Mensaje Error", JOptionPane.ERROR_MESSAGE);
            }


        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);

        }
                                            

        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        int numero_fila = this.jtabla.getSelectedRow();
        if (numero_fila != -1) {
            this.id_responsable.setText(this.jtabla.getValueAt(numero_fila, 0).toString());
            this.nombre.setText(this.jtabla.getValueAt(numero_fila, 1).toString());
            this.apellido.setText(this.jtabla.getValueAt(numero_fila, 2).toString());
            this.telefono.setText(this.jtabla.getValueAt(numero_fila, 3).toString());
            this.direccion.setText(this.jtabla.getValueAt(numero_fila, 4).toString());
            this.id_responsable.setEditable(false);

        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila", "Mensaje Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        conexion cnx = new conexion();
        Connection registro = cnx.conexion();
        String sql = "UPDATE responsable SET id_responsable='" + this.id_responsable.getText() + "',"
                                  + "nombre='" + this.nombre.getText() + "',"
                                    + "apellido='" + this.apellido.getText() + "',"
                                    + "telefono='" + this.telefono.getText() + "',"
                                     + "direccion='" + this.direccion.getText() + "' WHERE id_responsable='" + this.id_responsable.getText() + "'";
        try {
            PreparedStatement pst = (PreparedStatement) registro.prepareStatement(sql);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Se Actualizaron  Correctamente los datos", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            llenar_tabla();
            registro.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al Actualizar los Datos", "Mensaje Error", JOptionPane.ERROR_MESSAGE);
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

