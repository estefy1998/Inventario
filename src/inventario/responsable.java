private void llenar_tabla() {
        try {
            DefaultTableModel modelo;
            conexion cnx = new conexion();
            Connection registros = cnx.conexion();
            String[] nombre_atributos = {"id_responsable", "nombre","apellido","telefono","direccion"};
            String sql = ("SELECT *FROM responsable");
            modelo = new DefaultTableModel(null, nombre_atributos);
            Statement st = (Statement) registros.createStatement();
            ResultSet rs = st.executeQuery(sql);
            String[] filas = new String[6];

            while (rs.next()) {
                filas[0] = rs.getString("id_responsable");
                filas[1] = rs.getString("nombre");
                filas[2] = rs.getString("apellido");
                filas[3] = rs.getString("telefono");
                filas[4] = rs.getString("direccion");
                modelo.addRow(filas);

            }
            jtabla.setModel(modelo);
            registros.close();
        } catch (SQLException ex) {
            Logger.getLogger(responsable.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
 
